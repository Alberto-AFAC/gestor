function comentar10(){
  archivo = document.getElementById('archivo10').value;
  correodes = document.getElementById('correodes10').value;
  persona= document.getElementById('persona10').value;
  nombre = document.getElementById('nombre10').value;
  correo = document.getElementById('correo10').value;
  comentario = document.getElementById('comentario10').value;   

  valores(comentario,nombre,archivo,correo,persona);
   
    if(correodes == '' || persona == '0' || nombre == '' || correo == '' || comentario == ''){
        $('#aviso10').slideDown('slow');
        setTimeout(function()
        {
        $('#aviso10').slideUp('slow');
        },2500);
        }else{
        $('#exito10').slideDown('slow');
        setTimeout(function(){
        $('#exito10').slideUp('slow');
        },2000);
      var nombre=$('#nombre').val();
      var correo=$('#correo').val();
      var comentario=$('#comentario').val(); 
      var correodes=$('#correodes').val();
      var persona=$('#persona').val();
      var archivo=$('#archivo').val();
    $.ajax({
    //url:'http://grecoct.com/correo/correo.php',
    url:'./email.php',
    type:'POST',
    data:'nombre='+nombre+'&correo='+correo+'&comentario='+comentario+'&correodes='+correodes+'&persona='+persona+'&archivo='+archivo        
    }).done(function(respuesta){
    // alert(respuesta);
      if (respuesta==0) {
        $('#exito10').slideDown('slow');
        setTimeout(function(){
        $('#exito10').slideUp('slow');
        },2000);
        //  location.href="index.php";
        }else{
        $('#error10').slideDown('slow');
        setTimeout(function(){
        $('#error10').slideUp('slow');
        },2000);
      }
    });
  }
}
